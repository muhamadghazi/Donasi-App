    private void btnUbahPwActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahPwActionPerformed
        // TODO add your handling code here:
        String pwLama = txtPwLama.getText().trim();
        String pwBaru = new String(txtPwBaru.getPassword()).trim();
        String konfirmasi = new String(txtKomnfirPw.getPassword()).trim();

        if (pwLama.isEmpty() || pwBaru.isEmpty() || konfirmasi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        if (!pwBaru.equals(konfirmasi)) {
            JOptionPane.showMessageDialog(this, "Konfirmasi password tidak sama!");
            return;
        }

        try {
            Connection conn = Koneksi.getKoneksi();

            // DEBUG (cek username login)
            System.out.println("USERNAME LOGIN = " + formLogin.usernameLogin);

            String cekSql = "SELECT * FROM user WHERE username=? AND password=?";
            PreparedStatement cek = conn.prepareStatement(cekSql);
            cek.setString(1, formLogin.usernameLogin);
            cek.setString(2, pwLama);

            ResultSet rs = cek.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Password lama salah!");
                return;
            }

            String updateSql = "UPDATE user SET password=? WHERE username=?";
            PreparedStatement pst = conn.prepareStatement(updateSql);
            pst.setString(1, pwBaru);
            pst.setString(2, formLogin.usernameLogin);
            pst.executeUpdate(); // ðŸ”¥ INI YANG TADI HILANG

            JOptionPane.showMessageDialog(this, "Password berhasil diubah");
            dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnUbahPwActionPerformed

